#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*sd;

	sd = (char *)s;
	i = -1;
	if (n > 0)
	{
		while (n-- > 0)
			sd[++i] = 0;
	}
}
